"""
Train an SVM on a polynomial-kernel transformed dataset.

:authors Jason, Nick, Sam
"""

import numpy as np


def poly_kernel(x, x_i, p=4, c=1):
    """
    :param x_t: training input
    :param x_i: input vector
    :returns type int:
    """
    x_t = np.transpose(x)
    return (np.dot(x_t, x_i) + c)**p

